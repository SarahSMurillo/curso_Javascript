//Declarando Objetos
const persona={nombre:"Sarah",
    apellido:"Murillo"
}

//Sintaxis de punto
console.log(persona.nombre);

//Sintaxis otra menos comun
//Para imprimir 
console.log(persona["apellido"]);

//Poner otro atributo fuera de la declaracion
persona.log(persona)

//Para eliminar el atributo
delete persona.apellido;
console.log(persona);